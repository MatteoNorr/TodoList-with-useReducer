import Head from "next/head.js";
import { Context } from "@/store/index.js";
import TodoList from "../components/TodoList";
import { useReducer, useEffect } from "react";
import { check } from "@/store/reducers.js";
import { Login } from "./Login.jsx";
import { collection, getDocs } from "firebase/firestore";
import { db } from "../plugin/firebase";
import { useState } from "react";
import styles from "@/styles/Home.module.scss";

export default function Home({ data }) {
  const [state, dispatch] = useReducer(check, data);
  const [userInfo, setUserInfo] = useState("");

  return (
    <>
      <Head>
        <title>Next global state and Firebase</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Context.Provider value={{ state, dispatch }}>
        {userInfo === "" ? (
          <>
            <Login userInfo={userInfo} setUserInfo={setUserInfo} />
            <div className={styles.credential}>
              <h1>
                Inserisci l'username e accedi con Google per visualizzare la tua
                todolist
              </h1>
            </div>
          </>
        ) : (
          <>
            <Login userInfo={userInfo} setUserInfo={setUserInfo} />
            <TodoList />
          </>
        )}
      </Context.Provider>
    </>
  );
}

export async function getServerSideProps() {
  const data = [];

  const querySnapshot = await getDocs(collection(db, "todo-list"));

  querySnapshot.forEach((doc) => {
    data.push({ ...doc.data() });
  });

  return {
    props: {
      data,
    },
  };
}
